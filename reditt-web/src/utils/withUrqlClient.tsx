export const errorExchange: Exchange =
  ({ forward }) =>
  (ops$) => {
    return pipe(
      forward(ops$),
      tap(({ error }) => {
        if (error?.message.includes("user not authenticated")) {
          router.replace("/login");
        }
      })
    );
  };

